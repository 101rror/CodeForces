bool isPrime(int num)
{
    bool check = true;

    if(num <= 1)
    {
        check = false;
    }

    for (int i = 2; i < num / 2; i++)
    {
        if (num % i == 0)
        {
            check = false;
            break;
        }
    }
    return check;
}

