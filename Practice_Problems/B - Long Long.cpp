void passion()
{
    long long int n;
    cin >> n;

    long long int sum = 0, count = 0, flag = 0;

    for(int i = 0; i < n; i++)
    {
        int it;
        cin >> it;

        if(it <= 0)
        {
            if(it < 0)
            {
                flag = 1;
            }
        }
        else
        {
            if(flag == 1)
            {
                count++;
            }
            flag = 0;
        }
        if(it < 0)
        {
            it *= -1;
        }
        sum += it;
    }


    if(flag == 1)
    {
        count++;
    }

    cout << sum << " " << count;
    cout << endl;
}



