def passion():
    n = int(input())
    lst = list(map(int, input().split()))
    
    odd = 0
    even = 0

    for i in range(len(lst)):
        if(lst[i] % 2 == 0):
            even += 1
        else:
            odd += 1


    if(even % 2 != odd % 2):
        print("NO")
    else:
        if(even % 2 == 0):
            print("YES")
        else:
            for i in range(0, n):
                for j in range(i + 1, n):
                    x = lst[i]
                    y = lst[j]
                    s = abs(x - y)

                    if(x % 2 != y % 2 and s == 1):
                        print("YES")
                        return
                    
            print("NO")




def main():
    ToS = int(input())
    ToE = ToS

    while(ToE > 0):
        passion()
        ToE -= 1



